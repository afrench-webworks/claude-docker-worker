#!/bin/bash
# assemble.sh — Reads features.conf and composes the Dockerfile, settings.json,
# crontab, config.yaml.example, and entrypoint hooks from core + enabled features.
set -euo pipefail

PROJ_ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"

if ! command -v jq &>/dev/null; then
    echo "ERROR: jq is required but not found. Install it first:"
    echo "  macOS:   brew install jq"
    echo "  Linux:   sudo apt-get install jq"
    echo "  Windows: download jq.exe from https://github.com/jqlang/jq/releases"
    echo "           and place it in a directory on your PATH (e.g., C:\\Program Files\\Git\\usr\\bin\\)"
    exit 1
fi

FEATURES_CONF="$PROJ_ROOT/features.conf"
GENERATED_DIR="$PROJ_ROOT/.generated"
FEATURES_DIR="$PROJ_ROOT/features"

# ---------------------------------------------------------------------------
# Parse enabled features from features.conf
# ---------------------------------------------------------------------------

if [[ ! -f "$FEATURES_CONF" ]]; then
    echo "ERROR: features.conf not found. Copy features.conf.example and enable desired features."
    exit 1
fi

ENABLED_FEATURES=()
while IFS= read -r line; do
    line="${line%%#*}"          # strip comments
    line="${line// /}"          # strip spaces
    [[ -z "$line" ]] && continue
    ENABLED_FEATURES+=("$line")
done < "$FEATURES_CONF"

echo "Enabled features: ${ENABLED_FEATURES[*]:-none}"

# ---------------------------------------------------------------------------
# Validate features exist and check dependencies
# ---------------------------------------------------------------------------

for feat in "${ENABLED_FEATURES[@]}"; do
    feat_dir="$FEATURES_DIR/$feat"
    if [[ ! -d "$feat_dir" ]]; then
        echo "ERROR: Feature directory not found: $feat_dir"
        exit 1
    fi
    if [[ ! -f "$feat_dir/feature.yaml" ]]; then
        echo "ERROR: Missing feature.yaml in $feat_dir"
        exit 1
    fi

    # Check depends_on (simple grep-based YAML parse)
    deps=$(grep '^depends_on:' "$feat_dir/feature.yaml" | sed 's/^depends_on:[[:space:]]*//')
    if [[ "$deps" != "[]" && -n "$deps" ]]; then
        # Parse YAML list items: "  - dep_name"
        while IFS= read -r dep_line; do
            dep=$(echo "$dep_line" | sed 's/^[[:space:]]*-[[:space:]]*//' | sed 's/[[:space:]]*$//')
            [[ -z "$dep" ]] && continue
            found=false
            for enabled in "${ENABLED_FEATURES[@]}"; do
                [[ "$enabled" == "$dep" ]] && found=true && break
            done
            if ! $found; then
                echo "ERROR: Feature '$feat' depends on '$dep' which is not enabled in features.conf"
                exit 1
            fi
        done < <(sed -n '/^depends_on:/,/^[^ ]/p' "$feat_dir/feature.yaml" | grep '^ *-')
    fi
done

# ---------------------------------------------------------------------------
# Clean and prepare output directory
# ---------------------------------------------------------------------------

rm -rf "$GENERATED_DIR"
mkdir -p "$GENERATED_DIR/entrypoint.d"

# ---------------------------------------------------------------------------
# 1. Compose Dockerfile
# ---------------------------------------------------------------------------

{
    echo "# ============================================================"
    echo "# GENERATED by build/assemble.sh — do not edit directly"
    echo "# Enabled features: ${ENABLED_FEATURES[*]:-none}"
    echo "# ============================================================"
    echo ""
    cat "$PROJ_ROOT/core/Dockerfile.base"

    for feat in "${ENABLED_FEATURES[@]}"; do
        snippet="$FEATURES_DIR/$feat/Dockerfile.snippet"
        if [[ -f "$snippet" ]]; then
            echo ""
            echo "# --- Feature: $feat ---"
            cat "$snippet"
        fi
    done

    echo ""
    echo "# === Assembled artifacts ==="
    echo "RUN mkdir -p /root/.claude"
    echo "# Stage settings.json where it won't be shadowed by the claude-config volume."
    echo "# The entrypoint copies it onto the volume at boot so new permissions take effect."
    echo "COPY .generated/settings.json /opt/dockworker/settings.json"
    echo ""
    echo "# Project CLAUDE.md — assembled from core + feature snippets"
    echo "COPY .generated/CLAUDE.md /root/workspace/CLAUDE.md"
    echo ""
    echo "# Config — assembled from per-feature config snippets"
    echo "RUN mkdir -p /opt/dockworker"
    echo "COPY .generated/config.yaml /opt/dockworker/config.yaml"
    echo ""
    echo "# Cron schedule"
    echo "COPY .generated/crontab /etc/cron.d/dockworker"
    echo "RUN chmod 0644 /etc/cron.d/dockworker && crontab /etc/cron.d/dockworker"
    echo ""
    echo "# Entrypoint hooks from features"
    echo "COPY .generated/entrypoint.d/ /opt/entrypoint.d/"
    echo "RUN chmod +x /opt/entrypoint.d/*.sh 2>/dev/null || true"
    echo ""
    echo "# Core entrypoint"
    echo "COPY core/entrypoint.sh /entrypoint.sh"
    echo "RUN chmod +x /entrypoint.sh"
    echo ""
    echo 'ENTRYPOINT ["/entrypoint.sh"]'
} > "$GENERATED_DIR/Dockerfile"

echo "  Generated Dockerfile"

# ---------------------------------------------------------------------------
# 2. Merge settings.json
# ---------------------------------------------------------------------------

# Start with base permissions
cp "$PROJ_ROOT/core/settings.json.base" "$GENERATED_DIR/settings.json"

for feat in "${ENABLED_FEATURES[@]}"; do
    snippet="$FEATURES_DIR/$feat/settings.snippet.json"
    if [[ -f "$snippet" ]]; then
        # Merge: union of allow arrays, preserve deny
        merged=$(jq -s '
            .[0].permissions.allow = (.[0].permissions.allow + .[1].permissions.allow | unique) |
            .[0]
        ' "$GENERATED_DIR/settings.json" "$snippet")
        echo "$merged" > "$GENERATED_DIR/settings.json"
    fi
done

echo "  Generated settings.json"

# ---------------------------------------------------------------------------
# 3. Compose crontab
# ---------------------------------------------------------------------------

{
    echo "# Token keep-alive — every 6 hours, triggers OAuth refresh before expiry"
    echo "0 */6 * * * /bin/bash -l -c 'claude --model opus -p \"ping\" > /dev/null 2>&1'"

    for feat in "${ENABLED_FEATURES[@]}"; do
        snippet="$FEATURES_DIR/$feat/crontab.snippet"
        if [[ -f "$snippet" ]]; then
            echo ""
            echo "# --- Feature: $feat ---"
            cat "$snippet"
        fi
    done
    echo ""
} > "$GENERATED_DIR/crontab"

echo "  Generated crontab"

# ---------------------------------------------------------------------------
# 4. Compose config.yaml from per-feature config snippets
# ---------------------------------------------------------------------------

# For each enabled feature, ensure a user-edited config.snippet.yaml exists.
# If not, copy the example and tell the user to edit it.
missing_configs=()
for feat in "${ENABLED_FEATURES[@]}"; do
    example="$FEATURES_DIR/$feat/config.snippet.yaml.example"
    edited="$FEATURES_DIR/$feat/config.snippet.yaml"
    if [[ -f "$example" && ! -f "$edited" ]]; then
        cp "$example" "$edited"
        missing_configs+=("$feat")
    fi
done

if [[ ${#missing_configs[@]} -gt 0 ]]; then
    echo ""
    echo "  NOTE: Created config snippets from examples for: ${missing_configs[*]}"
    echo "  Edit these files with your values before building:"
    for feat in "${missing_configs[@]}"; do
        echo "    features/$feat/config.snippet.yaml"
    done
    echo ""
fi

# Concatenate enabled feature configs into the assembled config.yaml
{
    echo "# ============================================================"
    echo "# GENERATED by build/assemble.sh — do not edit directly"
    echo "# Edit per-feature configs in features/<name>/config.snippet.yaml"
    echo "# ============================================================"

    for feat in "${ENABLED_FEATURES[@]}"; do
        snippet="$FEATURES_DIR/$feat/config.snippet.yaml"
        if [[ -f "$snippet" ]]; then
            echo ""
            echo "# --- Feature: $feat ---"
            cat "$snippet"
        fi
    done
} > "$GENERATED_DIR/config.yaml"

echo "  Generated config.yaml"

# ---------------------------------------------------------------------------
# 5. Collect entrypoint hooks
# ---------------------------------------------------------------------------

hook_count=0
for feat in "${ENABLED_FEATURES[@]}"; do
    hook_dir="$FEATURES_DIR/$feat/entrypoint.d"
    if [[ -d "$hook_dir" ]]; then
        for hook in "$hook_dir"/*.sh; do
            [[ -f "$hook" ]] || continue
            cp "$hook" "$GENERATED_DIR/entrypoint.d/"
            hook_count=$((hook_count + 1))
        done
    fi
done

# Ensure at least one file exists so COPY doesn't fail
if [[ $hook_count -eq 0 ]]; then
    echo '#!/bin/bash' > "$GENERATED_DIR/entrypoint.d/.placeholder.sh"
    chmod +x "$GENERATED_DIR/entrypoint.d/.placeholder.sh"
fi

echo "  Collected $hook_count entrypoint hooks"

# ---------------------------------------------------------------------------
# 6. Compose CLAUDE.md from core + feature snippets
# ---------------------------------------------------------------------------

{
    cat "$PROJ_ROOT/CLAUDE.md"

    for feat in "${ENABLED_FEATURES[@]}"; do
        snippet="$FEATURES_DIR/$feat/claude.md.snippet"
        if [[ -f "$snippet" ]]; then
            echo ""
            cat "$snippet"
        fi
    done
} > "$GENERATED_DIR/CLAUDE.md"

echo "  Generated CLAUDE.md"

# ---------------------------------------------------------------------------
# Copy generated Dockerfile to project root for docker compose build
# ---------------------------------------------------------------------------

cp "$GENERATED_DIR/Dockerfile" "$PROJ_ROOT/Dockerfile"
echo ""
echo "Assembly complete. Generated files in .generated/"
echo "Dockerfile copied to project root."
echo ""
echo "Next steps:"
echo "  1. Edit per-feature configs in features/<name>/config.snippet.yaml"
echo "  2. docker compose build"
echo "  3. docker compose up -d"
